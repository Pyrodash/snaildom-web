    <li type="square">How do I buy clothes and shells?</li>
    There is a shop at the <b>West Forest</b> where you can buy clothes and shells.
    <br><br>
    <li type="square">How do I buy furniture?</li>
    There is a furniture store at the <b>West Forest</b> where you can get furniture every month.
